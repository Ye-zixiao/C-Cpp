#include<stdio.h>
#include<stdlib.h>
#include<string.h>
#define MAXTITL 40
#define MAXAUTL 40
#define MAXBKS 10
char* s_gets(char* st, int n);
struct book {
	char title[MAXTITL];
	char author[MAXAUTL];
	float value;
};

int main(void)
{
	struct book library[MAXBKS];
	int count = 0;
	int index, filecount;
	FILE* pbooks;
	int size = sizeof(struct book);

	if ((pbooks = fopen("book.dat", "a+b")) == NULL)							//以二进制添加模式打开（创建）文件
	{
		fprintf(stderr, "Can't open book.dat file.\n");
		exit(EXIT_FAILURE);
	}

	rewind(pbooks);
	while (count < MAXBKS && fread(&library[count], size, 1, pbooks) == 1)		//从文件中读取结构到程序中的结构数组当中，这样也可以为将count计数到目标位置
	{
		if (count == 0)
			puts("Current contents of book.dat:");
		printf("%s by %s: $%.2f\n", library[count].title,
			library[count].author, library[count].value);
		count++;
	}

	filecount=count;
	if (count == MAXBKS)														//在向文件输入型结构体之前需要检查下文件中的结构数组是否已满
	{
		fputs("The book.dat is full.", stderr);
		exit(EXIT_FAILURE);
	}

	puts("Please add new book titles.");
	puts("Press [enter] at the start of a line to stop.");
	while (count < MAXBKS && s_gets(library[count].title, MAXTITL) != NULL		//向文件中添加新的结构体
		&& library[count].title[0] != '\0')
	{
		puts("Now enter the author.");
		s_gets(library[count].author, MAXAUTL);
		puts("Now enter the value.");
		scanf("%f", &library[count].value);
		while (getchar() != '\n')
			continue;
        fwrite(&library[count],size,1,pbooks);                                  //事实上也可以在每一次读取完数据后就将一个结构写入到文件当中
        count++;
		if (count < MAXBKS)
			puts("Enter the next title.");
	}

	if (count > 0)
	{
		puts("Here is the list of your books:");								//再次打印文件中的数据，这部分的打印工作是从程序中的结构数组中读取的，而不是文件中
		for (index = 0; index < count; index++)
			printf("%s by %s: $%.2f\n", library[index].title,
				library[index].author, library[index].value);
		//fwrite(&library[filecount], size, count - filecount, pbooks);			//用fwrite函数向文件中添加后面自己添加的书本数据：总共具有count-filecount个数据
	}                                                                           /*使用文本编辑器打开该文件，虽然原先的文本内容可以查看，但是其数字部分会出现乱码*/
	else
	{
		puts("No books? Too bad.\n");
	}
	puts("Bye.\n");
	fclose(pbooks);
	return 0;
}

char* s_gets(char* st, int n)
{
	char* ret_val;
	char* find;

	ret_val = fgets(st, n, stdin);
	if (ret_val)
	{
		find = strchr(st, '\n');
		if (find)
			* find = '\0';
		else
			while (getchar() != '\n')
				continue;
	}

	return ret_val;
}
